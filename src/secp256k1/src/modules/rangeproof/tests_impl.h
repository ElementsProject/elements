static void print_vector_helper(unsigned char *buf, size_t buf_len) {
    size_t j;
    printf("    ");
    for (j = 0; j < buf_len; j++) {
        printf("0x%02x", buf[j]);
        if (j == buf_len-1) {
            printf(",\n");
        } else if ((j+1) % 16 != 0) {
            printf(", ");
        } else {
            printf(",\n");
            printf("    ");
        }
    }
    printf("};\n");
}

